    /**
     * Diese private Methode sendet die übergebenen Parameter in die Richtige
     * Reihenfolge für den Socket-Server,dabei wird nur die Lok damit angesprochen. Die  Socketverbindung
     * jeweils einmal auf- und nach dem Senden wieder abgebaut
     * @param typ Der Charakter gibt an ws gesteuert wird, dabei steht 'l' für Lok, 'w' für Weiche
     * @param befehl Beinhaltet den jeweiligen Befehl
     * @param adresse Führt die Adresse der Weiche oder der Lok
     */
    private void sendCommandLok(char typ, char befehl, char adresse){
        String sendeInformationen = typ + " " + befehl + " " + adresse;
        socketCon.connect();
        socketCon.sendStream(sendeInformationen);
        GesendetWeiche.setText(socketCon.readStream()); // Zum debugen
        socketCon.disconnect();

    }
    
    
    /**
     * Diese private Methode sendet die eigebenen Benutzerdaten. Dabei werden die
     * übergebenen Paramter in einen String zusammengefügt und gesendet.
     * @param Benutzer Der Benutzername
     * @param Password Das Password des Benutzers
     */
    private void sendCommandBenutzer(String Benutzer, String Password){ 
        String sendeInformationen = 'b' + " " + Benutzer + " " + Password; 
        socketCon.connect();
        socketCon.sendStream(sendeInformationen);
        int status = Integer.parseInt(socketCon.readStream());
        benutzer.setBenutzerStatus(status);
        socketCon.disconnect();
    }
    
    /**
     * Diese private Methode sendet die Befehle für die Weichenstellung. Dabei werden die
     * übergebenen Paramter in einen String zusammengefügt und gesendet.
     * @param befehl Ob die Weiche 'gerade' oder 'kurve' geschaltet werden soll
     * @param adresse Die Adresse der jeweiligen Weiche
     * @param Password Das Password des Benutzers
     */
    private void sendCommandWeiche(char befehl,char adresse){
        String sendeInformation = 'w' + " " + befehl + " " + adresse;
        socketCon.connect();
        socketCon.sendStream(sendeInformation);
        GesendetWeiche.setText(socketCon.readStream()); // Zum debugen
        socketCon.disconnect();
    }
    
    
